package com.mt.is.solr.exception;

public class VZWSolrException extends Exception{
	
	private static final long serialVersionUID = 8433724382430166775L;

	public VZWSolrException() {
		super();
	}

	public VZWSolrException(String message) {
		super(message);
	}

	public VZWSolrException(Throwable cause) {
		super(cause);
	}

	public VZWSolrException(String message, Throwable cause) {
		super(message, cause);
	}
	
}

